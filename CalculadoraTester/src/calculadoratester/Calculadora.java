/**
 *
 * @author gema
 */
public class Calculadora {
        
    public static double suma(double num,double num2){
        return num+num2;
    }
    public static double resta(double num,double num2){
        return num-num2;
    }
    public static double multiplicar(double num,double num2){
        return num*num2;
    }
    public static double dividir(double num,double num2){
        return num/num2;
    }
}
